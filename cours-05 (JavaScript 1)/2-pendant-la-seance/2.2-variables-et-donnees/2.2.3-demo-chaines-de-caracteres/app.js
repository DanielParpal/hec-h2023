// Chaînes de caractères
var prenom = "Daniel"; // les guillemets me permettent de délimiter ma chaîne de caractères
console.log(prenom);

// Les guillemets doubles fonctionnent aussi
var nomDeFamille = 'Parpal';
console.log(nomDeFamille);

// Les opérations disponibles sur les chaînes de caractères sont différentes de celles sur les nombres
// La concaténation permet de mettre bout à bout des morceaux de texte
console.log("Bonjour, " + prenom);

var phrase = "Bonjour " + "comment " + "allez-vous?";
console.log(phrase);

// Le console.log() nous offre une manière simple d'afficher plusieurs messages sur la même ligne
console.log("Allô", "Bonsoir", "Hola");

// Si nous avons des guillemets dans la chaîne de caractère:
console.log("J'ai faim");
console.log('Je leur ai dit: "comment-allez-vous"');
// si j'ai les 2 types de guillemets: je me sers de l'échappement
console.log('J\'ai dit: "comment allez-vous"');


